public class Clase1 {
    private int edad;
    private String nombre;

    //metodos accesores(getters y setters)

    //setter,establecer edad
    public void setEdad(int edad){
        this.edad = edad;
    }

    //getter, mostrar edad
    public int getEdad(){
        return edad;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getNombre() {
        return nombre;
    }
}
